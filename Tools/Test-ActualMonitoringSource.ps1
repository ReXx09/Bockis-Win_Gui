Write-Host "Was funktioniert OHNE LibreHardwareMonitor:" -ForegroundColor White
Write-Host "  • CPU-Last (Performance Counters) ✅" -ForegroundColor Green
Write-Host "  • RAM-Auslastung (WMI) ✅" -ForegroundColor Green
Write-Host "  • Disk I/O (Performance Counters) ✅" -ForegroundColor Green
Write-Host "  • GPU-Name (WMI) ✅" -ForegroundColor Green
Write-Host ""
Write-Host "Was NICHT funktioniert:" -ForegroundColor White
Write-Host "  • CPU-Temperaturen (Ring-0 nötig) ❌" -ForegroundColor Red
Write-Host "  • GPU-Temperatur/Frequenz/Power (Ring-0 nötig) ❌" -ForegroundColor Red
Write-Host "  • RAM-Temperatur (SMBus/Ring-0 nötig) ❌" -ForegroundColor Red
Write-Host "  • Lüftergeschwindigkeiten (Ring-0 nötig) ❌`n" -ForegroundColor Red

