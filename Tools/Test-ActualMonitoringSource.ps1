# Prüft, welche Sensor-Quellen WIRKLICH funktionieren

Write-Host "`n╔══════════════════════════════════════════════════════════════╗" -ForegroundColor Cyan
Write-Host "║  Sensor-Quellen Analyse (Ohne LibreHardwareMonitor)         ║" -ForegroundColor Cyan
Write-Host "╚══════════════════════════════════════════════════════════════╝`n" -ForegroundColor Cyan

# Import des Fallback-Moduls
Import-Module "$PSScriptRoot\..\Modules\Monitor\FallbackSensors.psm1" -Force

Write-Host "═══ 1. Performance Counters (CPU-Last) ═══" -ForegroundColor Yellow
try {
    $cpuLoad = Get-CpuLoadFallback
    if ($cpuLoad) {
        Write-Host "  ✅ CPU-Last: $cpuLoad%" -ForegroundColor Green
    } else {
        Write-Host "  ❌ CPU-Last nicht verfügbar" -ForegroundColor Red
    }
} catch {
    Write-Host "  ❌ Fehler: $_" -ForegroundColor Red
}

Write-Host "`n═══ 2. ACPI Thermal Zones (Temperaturen) ═══" -ForegroundColor Yellow
try {
    $thermalZones = Get-ThermalZonesFallback
    if ($thermalZones -and $thermalZones.Count -gt 0) {
        foreach ($zone in $thermalZones.GetEnumerator()) {
            Write-Host "  ✅ $($zone.Key): $($zone.Value)°C" -ForegroundColor Green
        }
    } else {
        Write-Host "  ⚠️  Keine ACPI Thermal Zones gefunden" -ForegroundColor Yellow
        Write-Host "      (Normal bei vielen Desktop-Mainboards)" -ForegroundColor Gray
    }
} catch {
    Write-Host "  ❌ Fehler: $_" -ForegroundColor Red
}

Write-Host "`n═══ 3. WMI Memory (RAM-Auslastung) ═══" -ForegroundColor Yellow
try {
    $memory = Get-MemoryUsageFallback
    if ($memory) {
        Write-Host "  ✅ RAM: $($memory.UsedGB) GB / $($memory.TotalGB) GB ($($memory.UsedPercent)%)" -ForegroundColor Green
    } else {
        Write-Host "  ❌ RAM-Auslastung nicht verfügbar" -ForegroundColor Red
    }
} catch {
    Write-Host "  ❌ Fehler: $_" -ForegroundColor Red
}

Write-Host "`n═══ 4. Disk Activity (I/O) ═══" -ForegroundColor Yellow
try {
    $disk = Get-DiskActivityFallback
    if ($disk) {
        Write-Host "  ✅ Reads: $($disk.ReadsPerSec)/s | Writes: $($disk.WritesPerSec)/s" -ForegroundColor Green
    } else {
        Write-Host "  ❌ Disk Activity nicht verfügbar" -ForegroundColor Red
    }
} catch {
    Write-Host "  ❌ Fehler: $_" -ForegroundColor Red
}

Write-Host "`n═══ 5. HWiNFO Shared Memory ═══" -ForegroundColor Yellow
$hwinfo = Test-HWiNFOAvailable
if ($hwinfo) {
    Write-Host "  ✅ HWiNFO läuft (könnte Sensor-Quelle sein!)" -ForegroundColor Green
} else {
    Write-Host "  ⚠️  HWiNFO nicht aktiv" -ForegroundColor Yellow
}

Write-Host "`n═══ 6. WMI GPU-Controller ═══" -ForegroundColor Yellow
try {
    $gpuControllers = Get-WmiObject -Namespace "root\CIMV2" -Class "Win32_VideoController" | 
        Where-Object { $_.Name -match "NVIDIA|AMD|Radeon|GeForce|Intel.*Graphics" }
    
    if ($gpuControllers) {
        foreach ($gpu in $gpuControllers) {
            Write-Host "  ✅ $($gpu.Name)" -ForegroundColor Green
            Write-Host "      Driver: $($gpu.DriverVersion)" -ForegroundColor Gray
            Write-Host "      Status: $($gpu.Status)" -ForegroundColor Gray
        }
    } else {
        Write-Host "  ❌ Keine GPU gefunden" -ForegroundColor Red
    }
} catch {
    Write-Host "  ❌ Fehler: $_" -ForegroundColor Red
}

Write-Host "`n═══ 7. LibreHardwareMonitor DLL ═══" -ForegroundColor Yellow
$dllPaths = @(
    "${env:LOCALAPPDATA}\Microsoft\WinGet\Packages\LibreHardwareMonitor*\LibreHardwareMonitorLib.dll",
    "${env:ProgramFiles}\LibreHardwareMonitor\LibreHardwareMonitorLib.dll",
    "$PSScriptRoot\..\Lib\LibreHardwareMonitorLib.dll"
)

$foundDll = $false
foreach ($path in $dllPaths) {
    $resolved = Resolve-Path $path -ErrorAction SilentlyContinue
    if ($resolved) {
        Write-Host "  ⚠️  LibreHWM DLL noch vorhanden: $($resolved.Path)" -ForegroundColor Yellow
        $foundDll = $true
    }
}

if (-not $foundDll) {
    Write-Host "  ✅ Keine LibreHWM DLL gefunden (korrekt deinstalliert)" -ForegroundColor Green
}

